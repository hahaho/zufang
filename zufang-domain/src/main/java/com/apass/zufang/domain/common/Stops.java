package com.apass.zufang.domain.common;
/**
 * 站点
 * @author zhanwendong
 *
 */
public class Stops {
	
	private Integer is_practical;
	
	private String name;
	
	private String uid;
	
	private double x;
	
	private double y;

	public Integer getIs_practical() {
		return is_practical;
	}

	public void setIs_practical(Integer is_practical) {
		this.is_practical = is_practical;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
	
}
